# set variable to 10
number = 10

# defining while loop, keep doing it while number is bigger
# or equal than 10
while number >= 10
# puts number  
  puts number
# increment number variable with 1 ( = add 1 to it and save it
# as well
  number += 1
# we condition our while loop, if the number is bigger than
# 100
  if number > 100
# break it, stop the whole while loop
    break
# end the if loop and the while loop
  end
end
